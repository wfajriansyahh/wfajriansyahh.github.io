                                PC mendengar hal ini tentu akan terpikirkan sebuah komputer dalam pikiran anda, Yup dan kali ini NekoPC akan memberikan kalian Tips mengenai Merakit PC Bekas, tentunya karena admin sendiri adalah orang yang pas pasan jadi admin kasih tips rakit pc dengan budjet yang sangat terjangkau buat pelajar.<hr>
                                <ul>
                                    <li>Prosessor</li>
                                    Admin rekomendasikan untuk PC pilihlah prosessor AMD Phenom x3 8750, Prosessor AMD sangatlah bagus untuk di tempatkan di PC, prosessor ini emang gampang panas tapi peformanya lancar jaya, dengan bantuan alat alat pendingin seperti kipas dll prosessor ini yang paling top di kalangan budjet paspasan, selain itu prosessor ini sangat pas untuk kalian yang suka ngegame juga. Harga prosessor ini kurang lebih sekitar Rp.150.000, ya itu sih harga bekasnya, harga new nya admin kurang tau, yang pasti nyari yang new juga sudah sangat susah soalnya prosessor ini sudah tidak lagi di produksi.
                                    <li>RAM</li>
                                    Admin rekomendasikan untuk RAM pilihlah merk Kingstone, soalnya di kalangan para RAM dan menurut pengalaman admin Kingstone inilah yang paling bagus, untuk RAM nya 4GB saja, 1x4 atau 2x2, Harga RAM Kingstone 1GB Rp.50.000, sedangkan untuk 2GB Rp.100.000, biasanya kalo beli lebih dari satu suka di diskon sih.
                                    <li>VGA</li>
                                    Admin rekomendasikan untuk VGA pilihlah VGA Nvidia GForce 9500 GT 500mb/128bit.
                                    VGA seperti ini sudah sangat memadai untuk beberapa game dan peformanya yang sudah sesuai dengan prosessor dan RAM nya. Harganya juga terjangkau sekitar Rp.200.000
                                    <li>Motherboard</li>
                                    Admin rekomendasikan untuk mobo pilihlah MOTHERBOARD ASROCK G31M-S LGA 775 DDR2, mobo keluaran asrock ini sangat pas sebagai penopang komponen komponen yang lainnya kalo berdasarkan pengalaman admin, jadi admin rekomendasikan saja mobo ini deh, harganya juga cukup murah, hanya Rp. 280.000 
                                    <li>CD-ROM</li>
                                    Admin rekomendasikan untuk CD ROM pilihlah CD ROM merk LG, karena dari semua CD ROM yang udah pernah admin coba hanya LG yang bagus dan umurnya panjang walaupun bekas, harganya murah yaitu Rp.50.000
                                    <li>Power Suppy</li>
                                    Admin rekomendasikan untuk PSU pilihlah PSU merk Icon 450 watt, dari desainnya saja PSU ini sudah bergaya gaming dengan kabel kabel yang dibungkus dengan karet bergaya gaming, selain itu prossesor ini juga umurnya panjang tentunya dengan harga yang murah yaitu hanya Rp.150.000 saja
                                    <li>HDD</li>
                                    Admin rekomendasikan pilihlah HDD merk WD atau Seagate, 2 merk ini sangat bagus dari peformanya di banding dengan merk lain, untuk kapasitasnya ambil 500 GB saja, jika kalian ingin peformanya lebih unggul lagi ambil saja HDD nya yang SATA, tapi kalau kalian masih mau berhemat ambil saja yang ATA, HDD 500GB SATA biasanya Rp.350.000 saja sedangkan yang ATA biasanya lebih murah yaitu Rp.250.000~ Rp.300.000
                                    <li>Case</li>
                                    Admin rekomendasikan untuk chasingnya yang biasa biasa aja jangan yang gaming, kenapa? soalnya kurang pas sama komponen komponen yang lainnya (dengan kata lain percuma hamburin duit) jadi pilih aja merk SPC harganya terjangkau cuman Rp.200.000 saja (NEW) untuk chasing admin rekomendasikan yang NEW soalnya chasing ini tugasnya sebagai rumah buat komponen komponen PC jadi harus yang kokoh, tenang aja walau chasing ini bukan gaming tapi chasing keluaran SPC ini masih keren kok dengan stylenya.
